function projectDocument({
  id,
  title,
  type,
  requiredForParticipation = true,
  status = "APPROVED",
  version = "v1.0",
}: {
  id: string;
  title: string;
  type: ProjectDocumentType;
  requiredForParticipation?: boolean;
  status?: ProjectDocumentStatus;
  version?: string;
}): ProjectDocument {
  return {
    id,
    title,
    type,
    version,
    status,
    issuedAt: "2026-05-29",
    uri: `ipfs://pending/${id}`,
    contentHash: `sha256:pending-${id}`,
    requiredForParticipation,
  };
}

